CREATE OR REPLACE VIEW `crime_pattern_finder` AS
SELECT
  `MethodUsed`,
  COUNT(*) AS `TotalCrimes`,
  GROUP_CONCAT(`CrimeID` ORDER BY `OccurredAt` SEPARATOR ', ') AS `CrimeIDs`
FROM `crime`
GROUP BY `MethodUsed`
ORDER BY `TotalCrimes` DESC, `MethodUsed`;

CREATE OR REPLACE VIEW `repeat_offender_alert` AS
SELECT
  c.`CriminalID`,
  c.`Name`,
  COUNT(ci.`CrimeID`) AS `NoOfCrimes`,
  CASE
    WHEN COUNT(ci.`CrimeID`) >= 2 THEN 'Repeat Criminal'
    ELSE 'Single Record'
  END AS `RepeatStatus`
FROM `criminal` c
LEFT JOIN `crime_involvement` ci ON c.`CriminalID` = ci.`CriminalID`
GROUP BY c.`CriminalID`, c.`Name`
ORDER BY `NoOfCrimes` DESC, c.`Name`;

CREATE OR REPLACE VIEW `bolo_list` AS
SELECT
  w.`WarrantID`,
  w.`IssueDate`,
  c.`CriminalID`,
  c.`Name`,
  c.`Age`,
  c.`Gender`,
  c.`PhysicalDescription`
FROM `warrant` w
JOIN `criminal` c ON w.`CriminalID` = c.`CriminalID`
WHERE w.`WarrantStatus` = 'Active'
ORDER BY w.`IssueDate` DESC;

UPDATE `criminal` c
JOIN (
  SELECT `CriminalID`, COUNT(*) AS `crime_count`
  FROM `crime_involvement`
  GROUP BY `CriminalID`
) ci ON c.`CriminalID` = ci.`CriminalID`
SET c.`IsRepeatOffender` = CASE
  WHEN ci.`crime_count` >= 2 THEN 1
  ELSE 0
END;

